defmodule LivePolls.Polls do
  @moduledoc """
  The Polls context.
  """

  import Ecto.Query, warn: false
  alias LivePolls.Repo

  alias LivePolls.Polls.Poll
  alias LivePolls.Polls.Option
  alias Phoenix.PubSub

  @pub_sub_topic_polls "polls"

  @doc """
  Returns the list of polls.

  ## Examples

      iex> list_polls()
      [%Poll{}, ...]

  """
  def list_polls do
    Repo.all(Poll)
  end

  def list_active_polls do
    now = DateTime.utc_now()

    Repo.all(
      from p in Poll,
        where: p.end_date > ^now,
        join: u in assoc(p, :user),
        left_join: o in assoc(p, :options),
        group_by: [p.id, u.id],
        select: %{
          id: p.id,
          title: p.title,
          description: p.description,
          end_date: p.end_date,
          username: u.username,
          total_votes: coalesce(sum(o.vote_count), 0)
        },
        order_by: [desc: :inserted_at]
    )
  end

  @doc """
  Gets a single poll.

  Raises `Ecto.NoResultsError` if the Poll does not exist.

  ## Examples

      iex> get_poll!(123)
      %Poll{}

      iex> get_poll!(456)
      ** (Ecto.NoResultsError)

  """
  def get_poll!(id), do: Repo.get!(Poll, id)

  @doc """
  Creates a poll.

  ## Examples

      iex> create_poll(%{field: value})
      {:ok, %Poll{}}

      iex> create_poll(%{field: bad_value})
      {:error, %Ecto.Changeset{}}

  """
  def create_poll(attrs \\ %{}) do
    options = attrs["options"] || []

    transformed_options =
      options
      |> Enum.map(&%{text: &1})
      |> Enum.map(&Option.changeset(%Option{}, &1))

    attrs = Map.delete(attrs, "options")

    new_poll =
      %Poll{}
      |> Poll.changeset(attrs)
      |> Ecto.Changeset.put_assoc(:options, transformed_options)
      |> Repo.insert()

    case new_poll do
      {:ok, poll} -> broadcast_new_poll(poll)
      _ -> new_poll
    end

    new_poll
  end

  defp broadcast_new_poll(%Poll{} = poll) do
    PubSub.broadcast(LivePolls.PubSub, @pub_sub_topic_polls, %{
      topic: "new_poll",
      payload: poll
    })
  end

  def subscribe_to_new_polls do
    PubSub.subscribe(LivePolls.PubSub, @pub_sub_topic_polls)
  end

  @doc """
  Updates a poll.

  ## Examples

      iex> update_poll(poll, %{field: new_value})
      {:ok, %Poll{}}

      iex> update_poll(poll, %{field: bad_value})
      {:error, %Ecto.Changeset{}}

  """
  def update_poll(%Poll{} = poll, attrs) do
    poll
    |> Poll.changeset(attrs)
    |> Repo.update()
  end

  @doc """
  Deletes a poll.

  ## Examples

      iex> delete_poll(poll)
      {:ok, %Poll{}}

      iex> delete_poll(poll)
      {:error, %Ecto.Changeset{}}

  """
  def delete_poll(%Poll{} = poll) do
    Repo.delete(poll)
  end

  @doc """
  Returns an `%Ecto.Changeset{}` for tracking poll changes.

  ## Examples

      iex> change_poll(poll)
      %Ecto.Changeset{data: %Poll{}}

  """
  def change_poll(%Poll{} = poll, attrs \\ %{}) do
    Poll.changeset(poll, attrs)
  end

